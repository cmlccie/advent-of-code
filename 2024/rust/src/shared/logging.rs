use anyhow::{Error, Result};

/*-------------------------------------------------------------------------------------------------
  Logging
-------------------------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------------
  Logging Functions
--------------------------------------------------------------------------------------*/

pub(crate) fn log_error(error: &Error) {
    log::error!("{}", error);
}

pub(crate) fn log_if_error<T>(result: &Result<T>) {
    if let Err(error) = result {
        log_error(error);
    }
}
